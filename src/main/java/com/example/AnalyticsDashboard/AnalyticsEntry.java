package com.example.AnalyticsDashboard;

public class AnalyticsEntry {
    private String date;
    private int metric;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    public int getMetric() {
        return metric;
    }
    public void setMetric(int metric) {
        this.metric = metric;
    }
}
